const { withAppBuildGradle } = require('expo/config-plugins');

const marker = '// MIB_BEACON_RELEASE_SIGNING';

module.exports = function withReleaseSigning(config) {
  return withAppBuildGradle(config, (next) => {
    let source = next.modResults.contents;
    if (source.includes(marker)) return next;
    const signingStart = '    signingConfigs {\n';
    if (!source.includes(signingStart)) {
      throw new Error('Could not locate Android signingConfigs block generated by Expo.');
    }
    const releaseSigning = `${signingStart}        ${marker}\n        release {\n            storeFile System.getenv('MIBBEACON_ANDROID_KEYSTORE') ? file(System.getenv('MIBBEACON_ANDROID_KEYSTORE')) : null\n            storePassword System.getenv('MIBBEACON_ANDROID_STORE_PASSWORD')\n            keyAlias System.getenv('MIBBEACON_ANDROID_KEY_ALIAS')\n            keyPassword System.getenv('MIBBEACON_ANDROID_KEY_PASSWORD')\n        }\n`;
    source = source.replace(signingStart, releaseSigning);
    const releaseBlock =
      /(buildTypes\s*\{[\s\S]*?release\s*\{[\s\S]*?)signingConfig signingConfigs\.debug/;
    if (!releaseBlock.test(source)) {
      throw new Error('Could not locate the Expo Android release signing fallback.');
    }
    source = source.replace(releaseBlock, '$1signingConfig signingConfigs.release');
    next.modResults.contents = source;
    return next;
  });
};
