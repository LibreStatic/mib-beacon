import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { pathToFileURL } from 'node:url';

const directory = resolve(dirname(fileURLToPath(import.meta.url)), '..');

function parseArguments(values) {
  const result = new Map();
  for (let index = 0; index < values.length; index += 2) {
    const key = values[index];
    const value = values[index + 1];
    if (!key?.startsWith('--') || value === undefined)
      throw new Error(`Invalid argument near ${key ?? '<end>'}`);
    result.set(key.slice(2), value);
  }
  return result;
}

export function immutableSources({ x64Url, x64Sha256, arm64Url, arm64Sha256 }) {
  for (const [label, url, sha] of [
    ['x64', x64Url, x64Sha256],
    ['arm64', arm64Url, arm64Sha256],
  ]) {
    if (!/^https:\/\//.test(url)) throw new Error(`${label} source must use HTTPS`);
    if (!/^[a-f0-9]{64}$/.test(sha))
      throw new Error(`${label} source requires a lowercase SHA-256`);
  }
  return `      # Immutable release payloads; generated by scripts/prepare-flatpak-release.mjs.\n      - type: archive\n        url: ${x64Url}\n        sha256: ${x64Sha256}\n        only-arches: [x86_64]\n      - type: archive\n        url: ${arm64Url}\n        sha256: ${arm64Sha256}\n        only-arches: [aarch64]`;
}

export function prepareManifest(template, options) {
  const localSource = /[ ]{6}# CI stages[\s\S]*?[ ]{6}- type: dir\n[ ]{8}path: staging/;
  if (!localSource.test(template)) throw new Error('Flatpak staging source marker was not found');
  return template.replace(localSource, immutableSources(options));
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const args = parseArguments(process.argv.slice(2));
  const version = args.get('version');
  if (!version) throw new Error('--version is required');
  const options = {
    x64Url: args.get('x64-url'),
    x64Sha256: args.get('x64-sha256'),
    arm64Url: args.get('arm64-url'),
    arm64Sha256: args.get('arm64-sha256'),
  };
  if (Object.values(options).some((value) => !value))
    throw new Error('Both architecture URLs and SHA-256 values are required');
  const template = readFileSync(resolve(directory, 'com.librestatic.mibbeacon.yml'), 'utf8');
  const output = args.get('output') ?? resolve(directory, 'com.librestatic.mibbeacon.release.yml');
  writeFileSync(output, prepareManifest(template, options));
  process.stdout.write(`Prepared immutable Flatpak manifest ${output} for ${version}.\n`);
}
